import java.util.Date;

public class Alunos {
    public Aluno(String nome, String cpf, String rg, String cnh, Date dataNascimento,
                 String residencia, String estadoCivil, String ocupacao, int altura,
                 int peso) {
        this.nome = nome;
        this.cpf = cpf;
        this.rg = rg;
        this.cnh = cnh;
        this.dataNascimento = dataNascimento;
        this.residencia = residencia;
        this.estadoCivil = estadoCivil;
        this.ocupacao = ocupacao;
        this.altura = altura;
        this.peso = peso;
    }

}
